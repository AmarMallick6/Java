package datatype;

public class Premitive {

	public static void main(String[] args) {
		          //integer DataType
		
	byte no=5;
	System.out.println("Number is "+ no);
	
	short age=55;
	System.out.println("Age is "+ age);
	
	int salary=50000;
	System.out.println("Salary is "+ salary);
	
	long turnover=500000;
	System.out.println("Company Turnover is "+turnover);
	
	            //Real Numbers...
	
	float size= 50.30002f;
	double weight=5.500;
	System.out.println(size);
	System.out.println(weight);
	
	//Boolean DataType ,It provide written type true or false,Bydefault boolean value is false.
	             
	//Character DataType, used for store single character datatype.............
	 char res = 'T';
	 System.out.println(res);
	}

}
