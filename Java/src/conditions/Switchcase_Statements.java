package conditions;

public class Switchcase_Statements {

	public static void main(String[] args) {
		char course='p';
		switch (course) {
		case 's':
			System.out.println("Selenium");
			break;
		case 'q':
			System.out.println("QTP");
			break;
		case 'c':
			System.out.println("Cypress");
			break;
		case 'p':
			System.out.println("Piaywright");
			break;
		default:
			System.out.println("Invalid Course");
		
		}
		

	}

}
