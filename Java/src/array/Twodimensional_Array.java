package array;

public class Twodimensional_Array {

	public static void main(String[] args) {
		int[][]mark= new int[2][2];
		//Add value in 2DArray..............................
		mark[0][0]=75;
		mark[0][1]=70;
		mark[1][0]=54;
		mark[1][1]=65;
		
	System.out.println("Number of students "+mark.length); //Size of Array.....
	//Acessing data......................
	System.out.println(mark[0][0]);
	System.out.println(mark[0][1]);
	System.out.println(mark[1][0]);
	System.out.println(mark[1][1]);
	
	}

}	
	
	
	
	
	
	
